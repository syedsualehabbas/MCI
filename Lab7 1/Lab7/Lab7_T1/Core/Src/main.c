/**
  * @brief Read ADC value from channel 1 (PA1)
  * @return 12-bit ADC value (0-4095)
  */
uint32_t Read_ADC(void)
{
    uint32_t adc_value = 0;
    
    // Start ADC conversion
    HAL_ADC_Start(&hadc1);
    
    // Wait for conversion to complete with timeout
    if (HAL_ADC_PollForConversion(&hadc1, 100) == HAL_OK)
    {
        adc_value = HAL_ADC_GetValue(&hadc1);
    }
    
    // Stop ADC
    HAL_ADC_Stop(&hadc1);
    
    return adc_value;
}

/**
  * @brief Read averaged ADC value (multiple samples)
  * @return Averaged 12-bit ADC value
  */
uint32_t Read_ADC_Averaged(void)
{
    uint32_t sum = 0;
    
    // Take multiple samples
    for (int i = 0; i < ADC_BUFFER_SIZE; i++)
    {
        sum += Read_ADC();
        HAL_Delay(1);  // Small delay between samples
    }
    
    return sum / ADC_BUFFER_SIZE;
}

/**
  * @brief Print ADC value with voltage (using integer math to avoid float issues)
  */
void Print_ADC_Value(uint32_t adc_value)
{
    // Calculate voltage in millivolts: (adc_value * 3300) / 4095
    uint32_t voltage_mv = (adc_value * ADC_REF_VOLTAGE) / ADC_MAX_VALUE;
    
    // Split into volts and millivolts for display
    uint32_t volts = voltage_mv / 1000;
    uint32_t millivolts = voltage_mv % 1000;
    
    UART_Printf("ADC: %4lu  |  Voltage: %lu.%03lu V\r\n", 
                adc_value, volts, millivolts);
}

